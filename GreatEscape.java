public class GreatEscape
{
	public static void main(String[]args)
	{
		
		
		System.out.println("This is the first line of GreatEscape.");
		System.out.print("\t This is line 2 of GreatEscape");
		System.out.println("\n\n\nThis is line 3 of GreatEscape");
		System.out.println("\t\t\t\t\tThis is line 4 of GreatEscape");

	
	}//end main
	
}//END OF CLASS